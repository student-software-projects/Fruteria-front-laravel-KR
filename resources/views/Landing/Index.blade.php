<H3>Mango</H3>
<img src="https://adncuba.com/sites/default/files/styles/mg_thumb_3/public/2020-07/Mangos.jpg?itok=BKWxQzcl" alt=""width="300px">
<p>
    El mango de la especie Mangifera indica es una fruta jugosa (más específicamente una drupa) y tiene numerosas variedades con formas, colores, texturas y sabores muy variables. Es redondo a oblongo y puede tener pesos de menos de 50 g a más de 2 kg. Consta de una cáscara, una pulpa comestible (el mesocarpio) y un endocarpio duro que contiene una semilla. Durante el desarrollo del fruto, la cáscara es de un color verde oscuro, que cambia a tonos verdes claros, amarillos, de color naranja, rojos o de color borra de vino al madurar el fruto. Según la variedad, la pulpa del mango maduro es dulce, de textura suave o fibrosa y de color naranja o amarillo. La variedad llamada mango de hilacha es la que mayor cantidad de fibra contiene.
    Cuando no ha madurado completamente, es de sabor medianamente ácido. Cuando aún está verde, su cáscara es de dicho color y su pulpa es de color amarillo verdoso muy pálido, consistencia dura y sabor ácido astringente.
    También el sabor es muy diferente entre una variedad y otra.
    Además del hombre, también algunas especies de insectos, pájaros, murciélagos y otros mamíferos comen mangos.
</p>
<H3>Guanabana</H3>
<img src="https://media.metrolatam.com/2019/10/30/gettyimages10548-8444cdbde380b7cad5fd27920d065cb5-1200x800.jpg" alt=""width="300px">
<p>
    La guanábana es una deliciosa fruta originaria de Perú, es el fruto de un árbol de la familia Annonaceae, genero Annona, y es conocida en algunas regiones de Suramérica como graviola. Este fruto se caracteriza por una corteza verde rugosa espinosa y una pulpa blanquecina muy blanda con grandes semillas de color negro.
</p>
<H3>Fresa</H3>
<img src="https://actualfruveg.com/wp-content/uploads/2018/03/fresa-strawberries-2-1201x640.jpg" alt=""width="300px">
<p>
    Son plantas herbáceas, perennifolias, con rizomas y estolones epigeos más o menos desarrollados, que enraízan en los nudos donde nacen hojas arrosetadas tri-partidas. Los tallos son generalmente simples, más o menos erectos y anuales. Las hojas se agrupan en falsas rosetas, con los segmentos ovalo-rómbicos, distalmente dentados. Las inflorescencias se organizan en cimas con brácteas. Las flores, hermafroditas o funcionalmente unisexuales, tienen un receptáculo con la zona axial algo cónica, acrescente y carnosa en la fructificación. Los 5 sépalos son lanceolados y en general enteros, más o menos acrescentes, erectos, patentes o reflejos en la fructificación. El calículo tiene 5 piezas más estrechas que los sépalos y son usualmente enteros. Los pétalos, en general en número de 5, son habitualmente mayores que los sépalos; son obovados, no escotados, con la uña corta, blancos, blanco-verdosos o de color crema. Hay unos 10-20 estambres y numerosos carpelos libres implantados en la zona axial del receptáculo. El fruto es un poliaquenio de aquenios ovoides incrustados en dicho receptáculo (eterio) que se vuelve carnoso al madurar.
</p>
