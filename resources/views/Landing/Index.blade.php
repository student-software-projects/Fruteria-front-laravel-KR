<H3>Papaya</H3>
<img src="https://www.lavanguardia.com/files/og_thumbnail/uploads/2019/07/03/5e997c2f07920.jpeg" alt=""width="300px">
<p>
    Carica papaya es una fruta muy rica arbustiva del género Carica en la familia Caricaceae. Su fruto se conoce comúnmente como papaya, papayón, olocotón, papayo, mamón, lechosa o lechoza .
    Carica del griego “karike”, nombre de una higuera. Papaya, deriva del maya “páapay-ya” que significa zapote jaspeado. Pertenece a la familia de las Caricaceae. La planta de papaya es una especie arborescente perennifolia.
</p>
